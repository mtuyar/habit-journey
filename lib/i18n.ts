export function useTranslation() {
  const lang = useSettingsStore(state => state.language) || 'tr';

  const t = (key: keyof typeof dictionaries.tr) => {
    return dictionaries[lang][key] || dictionaries['tr'][key] || key;
  };

  return { t, lang };
}
